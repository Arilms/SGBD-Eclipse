package apf3;

public class Escritorio {
	
	private int id_escritorio;
	private int id_equipamento;
	private int id_FuncionaE;
	private int id_empresa;
	
	public Escritorio() {
		
	}

	public int getId_escritorio() {
		return id_escritorio;
	}

	public void setId_escritorio(int id_escritorio) {
		this.id_escritorio = id_escritorio;
	}

	public int getId_equipamento() {
		return id_equipamento;
	}

	public void setId_equipamento(int id_equipamento) {
		this.id_equipamento = id_equipamento;
	}
	
	public int getId_Funcionae() {
		return id_FuncionaE;
	}

	public void setId_Funcionae(int id_FuncionaE) {
		this.id_FuncionaE = id_FuncionaE;
	}

	public int getId_empresa() {
		return id_empresa;
	}

	public void setId_empresa(int id_empresa) {
		this.id_empresa = id_empresa;
	}

}
